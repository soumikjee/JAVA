
// Default Value

class Default_V{

    static int i;
    static double j;
    static char k;
    static boolean l;
    static String m;
    
    public static void main( String [] args){
        System.out.println(Default_V.i);  //0
        System.out.println(Default_V.j);  //0.0
        System.out.println(Default_V.k);  //
        System.out.println(Default_V.l);  //false
        System.out.println(Default_V.m);  //null
    }
}